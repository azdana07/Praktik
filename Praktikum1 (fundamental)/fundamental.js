// Tipe data String
let name = "Azdana";
console.log("Name: " + name); // Menampilkan string

// Tipe data Number
let age = 20;
console.log("Age: " + age); // Menampilkan angka

// Tipe data Boolean
let isStudent = true;
console.log("Is student: " + isStudent); // Menampilkan boolean

// Tipe data Array
let colors = ["Pink", "Blue", "White"];
console.log("Colors: " + colors); // Menampilkan array

// Tipe data Undefined
let undefinedValue;
console.log("Undefined value: " + undefinedValue); // Menampilkan undefined

// Tipe data Null
let emptyValue = null;
console.log("Empty value: " + emptyValue); // Menampilkan null

// Menampilkan hasil dalam alert
alert("Name: " + name + "\nAge: " + age + "\nIs student: " + isStudent);
